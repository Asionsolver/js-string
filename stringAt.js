/**
@Javascript At method

The at() method returns the character at a specified index (position) in a string.
The at() method returns the same as [].

Syntax
string.at(index)

Parameters
1. index(Optional).
2. The index (position) of the character to be returned.
3. Default is 0.
4. -1 returns the last character.

Return Value
1. A String ----> A single character.
2. The character in the given position (index) in the string.
*/